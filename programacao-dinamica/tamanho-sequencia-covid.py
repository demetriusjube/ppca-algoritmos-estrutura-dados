# Devido a pandemia de COVID-19 os cientistas precisam do tamanho da mais longa sequência comum entre sequências de DNA. Desta forma, eles tem uma noção de quanto um trecho de código genético é similar ou não a outro. Faça um algoritmo que mostre o tamanho desta sequência.

# Entrada:

# Duas strings com as sequências. 

# Saída:

# O tamanho da mais longa sequência comum.



# Por exemplo, dada as sequências

# CAGGGTAGTC

# TCATATC

# a mais longa sequência comum é CATATC e seu tamanho é 6.

# For example:

# Input	Result
# CAGGGTAGTC
# TCATATC
# 6
